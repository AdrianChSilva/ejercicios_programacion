/**
Ejercicio 5
Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás
utilizando un bucle while.
 * @author AdrianChSilva
 */
public class Ejercicio05_T05 {
  public static void main(String[] args) {
    
    int x = 320;
        
    while (x > 140) {
      System.out.println(x);
      x -= 20;
    }
  }
}
