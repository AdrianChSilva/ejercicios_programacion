/**
 * Ejercicio 1
 * Muestra los números múltiplos de 5 de 0 a 100 
 * utilizando un bucle for.
 *
 * @author AdrianChSilva
 */
public class Ejercicio01_T05 {
  public static void main(String[] args) {
        
    for (int x = 0; x < 101; x += 5) { /** la expresión '+=' e utiliza para incrementar en la cantidad que señalemos*/
      System.out.println(x);
    }
  }
}
